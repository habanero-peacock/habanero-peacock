package no.karianneberg.yahtzee;

public class TwoPairs {
    private int first;
    private int second;

    public TwoPairs(int firstPairValue, int secondPairValue) {

        first = firstPairValue;
        second = secondPairValue;
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }
}

package no.karianneberg.yahtzee;

public class FullHouse {
    private int first;
    private int second;

    public FullHouse(int first, int second) {
        this.first = first;
        this.second = second;
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }
}
